package com.cta4j.train.station.model;

import org.jspecify.annotations.NullMarked;

/**
 * Represents the four cardinal directions.
 */
@NullMarked
public enum CardinalDirection {
    /**
     * North direction.
     */
    NORTH,

    /**
     * East direction.
     */
    EAST,

    /**
     * South direction.
     */
    SOUTH,

    /**
     * West direction.
     */
    WEST
}
